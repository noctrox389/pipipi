

function onBeatHit()
    if curBeat > 35 and curBeat < 61 then
        triggerEvent('Add Camera Zoom', 0.025,0.036)
    end

    if curBeat > 67 and curBeat < 157 then
        triggerEvent('Add Camera Zoom', 0.025,0.036)
    end

    if curBeat > 163 and curBeat < 225 then
        triggerEvent('Add Camera Zoom', 0.025,0.036)
    end

    if curBeat > 227 and curBeat < 256 then
        triggerEvent('Add Camera Zoom', 0.025,0.036)
    end

    if curBeat > 259 and curBeat < 321 then
        triggerEvent('Add Camera Zoom', 0.025,0.036)
    end

    if curBeat > 323 and curBeat < 389 then
        triggerEvent('Add Camera Zoom', 0.025,0.036)
    end

end